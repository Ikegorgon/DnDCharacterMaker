package charactermaker.model.features.feats.racialfeat;

public class ShiftingNature extends RacialFeats{
	public ShiftingNature() {
		super("Shifting Nature", "Shifter");
		this.setDescription("Shifting allows you to tap into your wilder side, and gain temporal bonuses depending on your heritage. As a reaction after receiving damage, you may shift, entering your more bestial state for one minute. You can end the shift at any moment as a bonus action. While you are in this state, you cannot cast spells that require concentration. You can’t shift again until you finish a long rest. At 5th level, you have achieved more control of your shifting. You can shift a second time before taking a long rest, and can shift at will as a bonus action.");
	}
}
